package vo.BE;

public class voChattingRoom {
	private String chattingRoomNo;
	private String floor;
	private int capacity;
	private String opentime_h;
	private String opentime_m;
	private String closetime_h;
	private String closetime_m;
	private int status;
	public String getChattingRoomNo() {
		return chattingRoomNo;
	}
	public void setChattingRoomNo(String chattingRoomNo) {
		this.chattingRoomNo = chattingRoomNo;
	}
	public String getFloor() {
		return floor;
	}
	public void setFloor(String floor) {
		this.floor = floor;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public String getOpentime_h() {
		return opentime_h;
	}
	public void setOpentime_h(String opentime_h) {
		this.opentime_h = opentime_h;
	}
	public String getOpentime_m() {
		return opentime_m;
	}
	public void setOpentime_m(String opentime_m) {
		this.opentime_m = opentime_m;
	}
	public String getClosetime_h() {
		return closetime_h;
	}
	public void setClosetime_h(String closetime_h) {
		this.closetime_h = closetime_h;
	}
	public String getClosetime_m() {
		return closetime_m;
	}
	public void setClosetime_m(String closetime_m) {
		this.closetime_m = closetime_m;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
		
}

